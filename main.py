"""
Math stuff
"""
def add(argument_a, argument_b):
    """
    Adds stuff
    """
    return argument_a+argument_b

def subtract(argument_a, argument_b):
    """
    Substracts stuff
    """
    return argument_a-argument_b

def divide(argument_a, argument_b):
    """
    Divides stuff
    """
    return argument_a//argument_b

def multiply(argument_a, argument_b):
    """
    Multiplies stuff
    """
    return argument_a*argument_b